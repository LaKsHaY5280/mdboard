import * as z from "zod";

// Shared validation schemas for profile management

export const profileUpdateSchema = z.object({
  firstName: z
    .string()
    .min(1, "First name is required")
    .min(2, "First name must be at least 2 characters")
    .max(50, "First name must be less than 50 characters")
    .regex(/^[a-zA-Z\s]+$/, "First name can only contain letters and spaces"),
  lastName: z
    .string()
    .min(1, "Last name is required")
    .min(2, "Last name must be at least 2 characters")
    .max(50, "Last name must be less than 50 characters")
    .regex(/^[a-zA-Z\s]+$/, "Last name can only contain letters and spaces"),
  email: z
    .string()
    .min(1, "Email is required")
    .email("Please enter a valid email address")
    .max(100, "Email must be less than 100 characters"),
  bio: z.string().max(200, "Bio must be less than 200 characters").optional(),
  interests: z
    .string()
    .max(100, "Interests must be less than 100 characters")
    .optional(),
});

export const passwordChangeSchema = z
  .object({
    currentPassword: z.string().min(1, "Current password is required"),
    newPassword: z
      .string()
      .min(8, "Password must be at least 8 characters")
      .max(100, "Password must be less than 100 characters")
      .regex(
        /^(?=.*[a-z])(?=.*[A-Z])(?=.*\d)(?=.*[@$!%*?&])[A-Za-z\d@$!%*?&]/,
        "Password must contain at least one uppercase letter, one lowercase letter, one number, and one special character"
      ),
    confirmPassword: z.string(),
  })
  .refine((data) => data.newPassword === data.confirmPassword, {
    message: "Passwords don't match",
    path: ["confirmPassword"],
  });

// Server-side only schema (without confirmPassword for API)
export const passwordChangeServerSchema = passwordChangeSchema.omit({
  confirmPassword: true,
});

// Type exports
export type ProfileUpdateValues = z.infer<typeof profileUpdateSchema>;
export type PasswordChangeValues = z.infer<typeof passwordChangeSchema>;
export type PasswordChangeServerData = z.infer<
  typeof passwordChangeServerSchema
>;
